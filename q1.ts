// Exercise 1:  Write a program that calculates the area of a rectangle.

// Define a function to calculate the area of a rectangle
function Areaofrectangle(length: number, width: number): number {
    return length * width;
  }
  
  // Example usage
  let rectangleLength: number = 10;
  let rectangleWidth: number = 8;
  
  let area: number = Areaofrectangle(rectangleLength, rectangleWidth);
  
  console.log(`The area of the rectangle is: ${area}`);



 