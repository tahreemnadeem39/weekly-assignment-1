"use strict";
//Exercise 3:  Write a program that checks if a given number is positive, negative, or zero.
let Input = 50;
if (Input > 0) {
    console.log(`The number ${Input} is positive.`);
}
else if (Input < 0) {
    console.log(`The number ${Input} is negative.`);
}
else {
    console.log(`The number ${Input} is zero.`);
}
